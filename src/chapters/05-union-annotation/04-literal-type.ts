// 리터럴 그 자체의 값을 타입으로 설정하는 것에 의미를 찾기는 어렵습니다. 😳

{
  let zero: 0 = 0;
  zero = -0.000001;

  let bookIcon: 'book' = 'book';
  bookIcon = '북';
}

// 하지만 리터럴 + 유니언을 조합하면 매우 유용한 타입 옵션을 관리할 수 있습니다. 😃

{
  type IconIds = 'book' | 'send' | 'check-mark' | 'close-eye';

  let iconType: IconIds = 'send';
  iconType = 'close-eye';
  iconType = 'books';
}
